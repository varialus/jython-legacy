package org.python.util.install;

/**
 * Some unicode special characters
 * 
 * @see http://www.unicode.org/charts/PDF/U0080.pdf
 */
public interface UnicodeSequences {

    public static final String A2 = "\u00C4"; // Ä: A with two dots above
    public static final String a2 = "\u00E4"; // ä: a with two dots above

    public static final String O2 = "\u00D6"; // Ö: O with two dots above
    public static final String o2 = "\u00F6"; // ö: o with two dots above

    public static final String U2 = "\u00DC"; // Ü: U with two dots above
    public static final String u2 = "\u00FC"; // ü: u with two dots above

}
